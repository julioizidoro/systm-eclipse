	public void gerarRelatorioDSFile(String nomeArquivo) throws JRException, IOException, SQLException {
		FacesContext facesContext = FacesContext.getCurrentInstance();
		ServletContext servletContext = (ServletContext) facesContext.getExternalContext().getContext();
		String nomeFtp = nomeArquivo;
		nomeArquivo = servletContext.getRealPath("/orcamento/" + nomeArquivo);
		FileWriter arquivoHTML = new FileWriter(new File(nomeArquivo));
		if (listaOrcamento.size() == 2) {
			arquivoHTML.write(gerarArquivo2());
		} else
			arquivoHTML.write(gerarArquivo3());
		arquivoHTML.close();
		FtpDadosFacade ftpDadosFacade = new FtpDadosFacade();
		Ftpdados dadosFTP = null;
		dadosFTP = ftpDadosFacade.getFTPDados();
		Ftp ftp = new Ftp(dadosFTP.getHostupload(), dadosFTP.getUser(), dadosFTP.getPassword());
		ftp.conectar();
		File fileHTML = new File(nomeArquivo);
		ftp.enviarArquivo(fileHTML, nomeFtp, "/systm/orcamento/");
		ftp.desconectar();
	}

